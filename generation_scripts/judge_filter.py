"""
generation_scripts/judge_filter.py

Standalone LLM-as-a-judge quality filter.
Can be run over any directory of task JSON files after generation.

Rotation policy (prevents preference leakage — Li et al., 2025):
  - Tasks generated by Claude/Anthropic → judged by OpenRouter dev-tier
  - Tasks generated by OpenRouter dev-tier → judged by Claude
  - Tasks with no synthesis_model → judged by dev-tier (default)

Usage:
    python generation_scripts/judge_filter.py \
        --input tenacious_bench_v0.1/train/ \
        --output tenacious_bench_v0.1/train_filtered/ \
        --spot_check 50 \
        --seed 42
"""

import argparse
import json
import os
import random
from pathlib import Path
from datetime import datetime, timezone

from dotenv import load_dotenv

load_dotenv(override=True)

POINTWISE_PROMPT = """You are a benchmark quality evaluator for B2B sales AI evaluation tasks.

Score this task on three dimensions (1-5 each):

1. input_coherence (1-5): Is the hiring signal brief realistic? Is the prospect profile internally consistent? Would a real B2B sales agent encounter this scenario?
2. ground_truth_verifiability (1-5): Can the ground_truth fields be checked programmatically (regex, string match) without ambiguity? Is the correct answer unambiguous?
3. rubric_clarity (1-5): Is it clear what passes vs fails? Can a different person apply the rubric and get the same score?

Inclusion thresholds: input_coherence >= 3, ground_truth_verifiability >= 4, rubric_clarity >= 3.

Task JSON:
{task}

Output ONLY this JSON (no other text):
{{"input_coherence": <1-5>, "ground_truth_verifiability": <1-5>, "rubric_clarity": <1-5>, "reasoning": "<one sentence why this passes or fails>"}}"""

PAIRWISE_PROMPT = """Two benchmark tasks target the same failure mode. Which is MORE DIAGNOSTIC — i.e., harder to solve by a generic agent, more grounded in realistic B2B sales behavior?

Task A:
{task_a}

Task B:
{task_b}

Output ONLY: {{"winner": "A" or "B", "reasoning": "<one sentence>"}}"""

THRESHOLDS = {"input_coherence": 3, "ground_truth_verifiability": 3, "rubric_clarity": 3}


def pick_judge_model(synthesis_model: str | None) -> str:
    """Rotate model family to prevent preference leakage."""
    if synthesis_model and ("claude" in synthesis_model.lower() or "anthropic" in synthesis_model.lower()):
        return "openrouter"
    return "anthropic"


def strip_fences(text: str) -> str:
    text = text.strip()
    if text.startswith("```"):
        lines = text.split("\n")[1:]
        if lines and lines[-1].strip() == "```":
            lines = lines[:-1]
        text = "\n".join(lines).strip()
    return text


def call_judge(prompt: str, judge_type: str) -> str:
    from openai import OpenAI
    if judge_type == "anthropic":
        model = "anthropic/claude-haiku-4-5"
    else:
        model = "qwen/qwen3-8b"
    client = OpenAI(
        base_url="https://openrouter.ai/api/v1",
        api_key=os.environ["ANTHROPIC_API_KEY"],
    )
    r = client.chat.completions.create(
        model=model,
        max_tokens=256,
        messages=[{"role": "user", "content": prompt}],
    )
    return strip_fences(r.choices[0].message.content)


def pointwise_score(task: dict) -> tuple[bool, dict]:
    synthesis_model = task.get("metadata", {}).get("synthesis_model")
    judge_type = pick_judge_model(synthesis_model)
    prompt = POINTWISE_PROMPT.format(task=json.dumps(task, indent=2))
    try:
        raw = call_judge(prompt, judge_type)
        scores = json.loads(raw)
        passed = all(scores.get(k, 0) >= v for k, v in THRESHOLDS.items())
        scores["judge_type"] = judge_type
        return passed, scores
    except Exception as e:
        return False, {"error": str(e), "judge_type": judge_type}


def pairwise_compare(task_a: dict, task_b: dict, judge_type: str = "openrouter") -> str:
    prompt = PAIRWISE_PROMPT.format(
        task_a=json.dumps(task_a, indent=2),
        task_b=json.dumps(task_b, indent=2),
    )
    try:
        raw = call_judge(prompt, judge_type)
        result = json.loads(raw)
        return result.get("winner", "A")
    except Exception:
        return "A"


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--input", required=True)
    parser.add_argument("--output", required=True)
    parser.add_argument("--spot_check", type=int, default=50, help="Number of tasks to spot-check with eval-tier model")
    parser.add_argument("--seed", type=int, default=42)
    args = parser.parse_args()

    rng = random.Random(args.seed)
    input_dir = Path(args.input)
    output_dir = Path(args.output)
    output_dir.mkdir(parents=True, exist_ok=True)

    tasks = []
    for p in sorted(input_dir.glob("*.json")):
        with open(p) as f:
            tasks.append(json.load(f))
    print(f"Loaded {len(tasks)} tasks from {args.input}")

    # Only LLM-generated tasks need judge filtering; code-generated tasks pass automatically
    AUTO_PASS_MODES = {"trace-derived", "programmatic"}

    passed_tasks = []
    failed_tasks = []
    for i, task in enumerate(tasks):
        if task.get("source_mode") in AUTO_PASS_MODES:
            task.setdefault("metadata", {})["judge_scores"] = {"auto_pass": True}
            passed_tasks.append(task)
        else:
            passed, scores = pointwise_score(task)
            task.setdefault("metadata", {})["judge_scores"] = scores
            if passed:
                passed_tasks.append(task)
            else:
                failed_tasks.append(task)
        if (i + 1) % 10 == 0:
            print(f"  Processed {i+1}/{len(tasks)} | Passed so far: {len(passed_tasks)}")

    print(f"\nFilter result: {len(passed_tasks)} passed / {len(failed_tasks)} failed")

    # Spot-check with eval-tier model for calibration
    spot_sample = rng.sample(passed_tasks, min(args.spot_check, len(passed_tasks)))
    print(f"\nSpot-checking {len(spot_sample)} tasks with eval-tier model (claude-haiku-4-5)...")
    spot_results = []
    for task in spot_sample:
        prompt = POINTWISE_PROMPT.format(task=json.dumps(task, indent=2))
        try:
            raw = call_judge(prompt, "anthropic")
            spot_score = json.loads(raw)
            spot_score["task_id"] = task["task_id"]
            spot_results.append(spot_score)
        except Exception as e:
            spot_results.append({"task_id": task["task_id"], "error": str(e)})

    # Write filtered tasks
    for task in passed_tasks:
        out_path = output_dir / f"{task['task_id']}.json"
        with open(out_path, "w") as f:
            json.dump(task, f, indent=2)

    # Write filter log
    log = {
        "run_at": datetime.now(timezone.utc).isoformat(),
        "input_count": len(tasks),
        "passed_count": len(passed_tasks),
        "failed_count": len(failed_tasks),
        "spot_check_count": len(spot_results),
        "spot_check_results": spot_results,
        "failed_task_ids": [t["task_id"] for t in failed_tasks],
    }
    with open(output_dir / "judge_filter_log.json", "w") as f:
        json.dump(log, f, indent=2)

    print(f"\nFiltered tasks written to {args.output}")
    print(f"Filter log: {output_dir / 'judge_filter_log.json'}")


if __name__ == "__main__":
    main()
